package com.ec.usrcore.config;

public class EpGateConfig {
	
	private int epGateId;
	private String ip;
	private int port;
	
	public int getEpGateId() {
		return epGateId;
	}
	public void setEpGateId(int epGateId) {
		this.epGateId = epGateId;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	
	

}
